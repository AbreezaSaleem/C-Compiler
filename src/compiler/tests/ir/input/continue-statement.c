
int main(void) {
  int i;
  i = 0;
  while (i < 5) {
    if (i % 3 == 0) {
      continue;
    } else {
      !i;
    }
    i = 99;
  }
  
  do{
    if (i % 3 == 0) {
      continue;
    } else {
      !i;
    }
    i = 99;
  } while  (i < 5);

  for (i = 0; i < 5; i+1) {
    if (i % 3 == 0) {
      continue;
    } else {
      !i;
    }
    i = 99;
  }
  return 0;
}


